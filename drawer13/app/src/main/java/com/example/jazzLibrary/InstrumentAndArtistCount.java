package com.example.jazzLibrary;

public class InstrumentAndArtistCount {

    private int instrument_id;
    private String instrument_name;
    private int artist_count;


    public int getInstrument_id() {
        return instrument_id;
    }

    public void setInstrument_id(int instrument_id) {
        this.instrument_id = instrument_id;
    }

    public String getInstrument_name() {
        return instrument_name;
    }

    public void setInstrument_name(String instrument_name) {
        this.instrument_name = instrument_name;
    }

    public int getArtist_count() {
        return artist_count;
    }

    public void setArtist_count(int artist_count) {
        this.artist_count = artist_count;
    }
}
